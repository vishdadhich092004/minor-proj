import '../providers/language_provider.dart';

// Translation extension for easy access
extension TranslationExtension on BuildContext {
  Future<String> translate(String text) async {
    final languageProvider = Provider.of<LanguageProvider>(this, listen: false);
    return await languageProvider.translate(text);
  }

  String get currentLanguageCode {
    final languageProvider = Provider.of<LanguageProvider>(this, listen: false);
    return languageProvider.currentLanguageCode;
  }

  AppLanguage get currentLanguage {
    final languageProvider = Provider.of<LanguageProvider>(this, listen: false);
    return languageProvider.currentLanguage;
  }
}

